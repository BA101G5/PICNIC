package com.login.model;

public class LogInVO {
	private String adm_no;
	private String adm_acc;
	private String adm_pw;
	private String adm_iden;
	private String adm_name;
	private String adm_sta;
	public String getAdm_no() {
		return adm_no;
	}
	public void setAdm_no(String adm_no) {
		this.adm_no = adm_no;
	}
	public String getAdm_acc() {
		return adm_acc;
	}
	public void setAdm_acc(String adm_acc) {
		this.adm_acc = adm_acc;
	}
	public String getAdm_pw() {
		return adm_pw;
	}
	public void setAdm_pw(String adm_pw) {
		this.adm_pw = adm_pw;
	}
	public String getAdm_iden() {
		return adm_iden;
	}
	public void setAdm_iden(String adm_iden) {
		this.adm_iden = adm_iden;
	}
	public String getAdm_name() {
		return adm_name;
	}
	public void setAdm_name(String adm_name) {
		this.adm_name = adm_name;
	}
	public String getAdm_sta() {
		return adm_sta;
	}
	public void setAdm_sta(String adm_sta) {
		this.adm_sta = adm_sta;
	}
	
	
}
